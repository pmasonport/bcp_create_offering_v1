-- Feature aggregation defines how to combine features across entitlements
-- feature_aggregation: How to combine the same feature across multiple entitlements
-- IMPORTANT: These are offering declarations, NOT SQL aggregation functions
-- Application code implements the aggregation logic based on these values
-- Do NOT use SQL SUM(), MAX() - they won't respect business rules
CREATE TYPE feature_aggregation AS ENUM (
    'sum',     -- Add values together (e.g., seats: 100 + 50 = 150)
    'max',     -- Take highest value (e.g., storage: max(1TB, 500GB) = 1TB)
    'newest'   -- Use most recent value (e.g., support tier: newest subscription wins)
);

