    offering_id docker_id NOT NULL REFERENCES offerings(id) ON DELETE RESTRICT,
    rate_card_id docker_id NOT NULL,
    service_id docker_id NOT NULL REFERENCES services(id) ON DELETE RESTRICT,
    feature_id docker_id NOT NULL,
    value TEXT NOT NULL,  -- Purchased quantity or override
    purchase_date TIMESTAMPTZ NOT NULL,
    expires_at TIMESTAMPTZ,  -- When prepaid purchases expire (NULL for non-expiring)
    expired_at TIMESTAMPTZ,  -- NULL = active
    expiration_reason expiration_reason,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    version INTEGER NOT NULL DEFAULT 1,

    -- Composite FKs to enforce referential integrity
    FOREIGN KEY (service_id, feature_id) REFERENCES service_features(service_id, id) ON DELETE RESTRICT,
    FOREIGN KEY (rate_card_id, service_id) REFERENCES rate_cards(id, service_id) ON DELETE RESTRICT
);

CREATE INDEX idx_account_purchases_account ON account_purchases(account_id);
CREATE INDEX idx_account_purchases_rate_card ON account_purchases(account_id, rate_card_id);
-- Partial indexes for hot-path queries (only active rows)
CREATE INDEX idx_account_purchases_active_offering ON account_purchases(account_id, offering_id) WHERE expired_at IS NULL;
CREATE INDEX idx_account_purchases_active_feature ON account_purchases(account_id, service_id, feature_id) WHERE expired_at IS NULL;

COMMENT ON TABLE account_purchases IS 'Individual feature purchases - seats, build minutes, storage, etc. (free or paid)';
