-- 3. RATE CARDS
-- =============================================================================

-- Rate cards use Type+JSONB pattern: a single consolidated table with a pricing_type
-- discriminator and a pricing_details JSONB column. This eliminates the need for 6
-- separate pricing child tables and avoids complex multi-table JOINs.
-- See 02-data-model.md for the JSONB structure per pricing type.

CREATE TABLE rate_cards (
    id docker_id PRIMARY KEY,
    offering_id docker_id NOT NULL REFERENCES offerings(id) ON DELETE RESTRICT,
    service_id docker_id NOT NULL REFERENCES services(id) ON DELETE RESTRICT,
    feature_id docker_id NOT NULL,
    list_price BOOLEAN NOT NULL DEFAULT true,
    account_id docker_id,  -- NULL for list prices, specific account for custom pricing

    -- Type+JSONB: pricing_type discriminator + pricing_details blob
    pricing_type pricing_model NOT NULL,
    pricing_details JSONB NOT NULL,

    billing_timing billing_timing NOT NULL,
    billing_cycle billing_cycle NOT NULL,
    currency VARCHAR(3) NOT NULL DEFAULT 'USD',
    external_price_id TEXT,
    external_system VARCHAR(50),
    effective_date TIMESTAMPTZ NOT NULL,
    expiration_date TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    version INTEGER NOT NULL DEFAULT 1,

    UNIQUE(offering_id, service_id, feature_id, effective_date, account_id),
    UNIQUE(id, service_id),  -- Composite unique to enable composite FK from account_purchases
    FOREIGN KEY (service_id, feature_id) REFERENCES service_features(service_id, id) ON DELETE RESTRICT,

    -- JSONB validation: ensure required keys exist per pricing type
    CONSTRAINT valid_per_unit CHECK (
        pricing_type != 'per_unit' OR (
            pricing_details ? 'resource_id' AND
            pricing_details ? 'unit_price'
        )
    ),
    CONSTRAINT valid_fixed CHECK (
        pricing_type != 'fixed' OR (
            pricing_details ? 'resource_id' AND
            pricing_details ? 'fixed_price'
        )
    ),
    CONSTRAINT valid_fixed_plus_per_unit CHECK (
        pricing_type != 'fixed_plus_per_unit' OR (
            pricing_details ? 'resource_id' AND
            pricing_details ? 'base_price' AND
            pricing_details ? 'unit_price'
        )
    ),
    CONSTRAINT valid_graduated CHECK (
        pricing_type != 'graduated' OR (
            pricing_details ? 'resource_id' AND
            pricing_details ? 'tiers'
        )
    ),
    CONSTRAINT valid_volume CHECK (
        pricing_type != 'volume' OR (
            pricing_details ? 'resource_id' AND
            pricing_details ? 'tiers'
        )
    ),
    CONSTRAINT valid_block CHECK (
        pricing_type != 'block' OR (
            pricing_details ? 'resource_id' AND
            pricing_details ? 'blocks'
        )
    )
);

CREATE INDEX idx_rate_cards_offering ON rate_cards(offering_id);
CREATE INDEX idx_rate_cards_feature ON rate_cards(feature_id);
CREATE INDEX idx_rate_cards_pricing_type ON rate_cards(pricing_type);
CREATE INDEX idx_rate_cards_external_price ON rate_cards(external_price_id, external_system);
CREATE INDEX idx_rate_cards_account ON rate_cards(account_id) WHERE account_id IS NOT NULL;

COMMENT ON TABLE rate_cards IS 'Pricing definitions using Type+JSONB pattern. pricing_type discriminates the JSONB structure in pricing_details.';
COMMENT ON COLUMN rate_cards.id IS 'Primary key with prefixed unique identifier';
COMMENT ON COLUMN rate_cards.list_price IS 'true = list price, false = customer-specific';
COMMENT ON COLUMN rate_cards.account_id IS 'NULL for list prices, specific account for custom pricing';
COMMENT ON COLUMN rate_cards.pricing_type IS 'Discriminator: fixed, per_unit, graduated, volume, block, fixed_plus_per_unit';
