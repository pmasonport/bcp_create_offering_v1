-- Service features define individual capabilities within a service
-- IMPORTANT: feature_aggregation is defined here (not in rate_cards) to prevent conflicts
-- When a customer has multiple offerings with the same feature, we need ONE canonical
-- aggregation approach. By defining aggregation at the feature level, we ensure semantic
-- correctness: seats always sum, storage limits always use max, etc. This prevents impossible
-- configurations where different rate cards could declare conflicting aggregation for the same
-- feature.
-- Set feature_aggregation when creating rate cards that allow multiple purchases of this feature.
CREATE TABLE service_features (
    id docker_id PRIMARY KEY,
    service_id docker_id NOT NULL REFERENCES services(id) ON DELETE RESTRICT,
    slug VARCHAR(63) NOT NULL,
    feature_name VARCHAR(255) NOT NULL,
    description TEXT,
    feature_type feature_type NOT NULL,
    mutable BOOLEAN NOT NULL DEFAULT false,
    metering_type metering_type,
    feature_aggregation feature_aggregation,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    version INTEGER NOT NULL DEFAULT 1,

    UNIQUE(service_id, slug),
    UNIQUE(service_id, id)  -- Composite unique to enable composite FKs
);

CREATE INDEX idx_service_features_service ON service_features(service_id);
-- Note: No explicit index on (service_id, slug) needed — UNIQUE constraint creates one automatically.

COMMENT ON TABLE service_features IS 'Individual features within a service';
COMMENT ON COLUMN service_features.id IS 'Primary key with prefixed unique identifier';
COMMENT ON COLUMN service_features.mutable IS 'Can feature value change through purchase/ops (NOT customer self-service settings)';
COMMENT ON COLUMN service_features.metering_type IS 'How feature usage is tracked (nullable for non-metered features)';
COMMENT ON COLUMN service_features.feature_aggregation IS 'How to combine this feature across multiple purchases/entitlements (sum/max/newest). NULL = no aggregation needed or not yet configured. Set when creating rate cards that enable multiple purchases. Defined at feature level to prevent conflicts when customer has multiple offerings with same feature.';

